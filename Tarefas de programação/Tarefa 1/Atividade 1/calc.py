class Calculadora:

    def __init__(self) -> None:
        pass

    def soma(self, operando_a: float, operando_b: float) -> float:
        return operando_a + operando_b

    def subtracao(self, operando_a: float, operando_b: float) -> float:
        return operando_a - operando_b
    
    def multiplicacao(self, operando_a: float, operando_b: float) -> float:
        return operando_a * operando_b
    
    def divisao(self, operando_a: float, operando_b: float) -> float:
        return operando_a / operando_b
    