def soma(operando_a: float, operando_b: float) -> float:
    return operando_a + operando_b

def subtracao(operando_a: float, operando_b: float) -> float:
    return operando_a - operando_b
    
def multiplicacao(operando_a: float, operando_b: float) -> float:
    return operando_a * operando_b
    
def divisao(operando_a: float, operando_b: float) -> float:
    return operando_a / operando_b